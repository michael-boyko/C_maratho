#include <assert.h>
#include <stdbool.h>

int main() {
    assert(mx_max(-3, 9, 200) == 200);
    assert(mx_max(-20000000, 0, 20000000) == 20000000);
    assert(mx_max(-15000000, -5000000, 10000000) == 10000000);
    assert(mx_max(0, 0, 0) == 0);
    assert(mx_max(20000000, -20000000, 0) == 20000000);
    assert(mx_max(10000000, 20000000, -15000000) == 20000000);
    assert(mx_max(5000000, 0, -5000000) == 5000000);
    assert(mx_max(-20000000, -10000000, -15000000) == -10000000);
    assert(mx_max(20000000, 15000000, 10000000) == 20000000);
    assert(mx_max(0, -20000000, 20000000) == 20000000);
    assert(mx_max(-10000000, 5000000, 10000000) == 10000000);
	return 0;
}
